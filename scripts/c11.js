function draw(){
  translate(windowWidth/2,windowHeight/2);
  push();
  rotate((hour()+minute()/60)/24*PI);
  scale(.78);
  drawbitmap(dial,  16, dcolors, 20);
  pop();
  drawbitmap(clock, 18, ccolors, 20);
}

function drawbitmap(bitmap, width, colors = ['#000', '#fff'], scale = 1) {
  let m = width/2*scale;
  for (var i = 0; i < bitmap.length; i++) {
    stroke(colors[bitmap[i]]);
    fill(colors[bitmap[i]]);
    rect((i%width*scale)-m, floor(i/width)*scale-m, scale, scale);
  }
}

